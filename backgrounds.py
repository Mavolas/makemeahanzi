def _generated_fill_to_200() -> list[tuple[str, str]]:
    """用黄金角分布色相，补足到总数 200。"""
    need = 200 - len(_NAMED_PASTELS)
    if need <= 0:
        return []
    out: list[tuple[str, str]] = []
    for i in range(need):
        h = (i * 0.618033988749895) % 1.0
        l = 0.90 + (i % 6) * 0.008
        s = 0.10 + (i % 8) * 0.018
        hx = _hsl_to_hex(h, s, l)
        out.append((f"浅韵{i + 1:03d}", hx))
    return out


def _build_palette() -> list[CanvasBackground]:
    rows = list(_NAMED_PASTELS) + _generated_fill_to_200()
    return [CanvasBackground(n, h) for n, h in rows[:200]]


CANVAS_BACKGROUND_COLORS: list[CanvasBackground] = _build_palette()


def pick_random_canvas_background() -> CanvasBackground:
    """随机选一色（具名 + 浅韵编号，共 200）。"""
    return random.choice(CANVAS_BACKGROUND_COLORS)


def pick_random_canvas_bg_hex() -> str:
    return pick_random_canvas_background().hex
