def _hsl_to_hex(h: float, s: float, l: float) -> str:
    """h,s,l ∈ [0,1]，输出 #RRGGBB。"""
    r, g, b = colorsys.hls_to_rgb(h % 1.0, l, s)
    return "#{:02x}{:02x}{:02x}".format(
        min(255, max(0, int(round(r * 255)))),
        min(255, max(0, int(round(g * 255)))),
        min(255, max(0, int(round(b * 255)))),
    )


